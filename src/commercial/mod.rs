//!
//! The OSS core (everything outside this module) is AGPL-3.0-or-later
//! and ships without any of the capabilities listed in `MONETIZATION.md`.
//! This module itself is governed by `LICENSE-COMMERCIAL` at the repo
//! root (Forseti Commercial License 1.0, SPDX
//! `LicenseRef-Forseti-Commercial-1.0`) — source-available, free for
//! development / evaluation / personal hobbyist use, requires a valid
//! Ed25519-signed Commercial License Key for production use, and forbids
//! operating Forseti as a hosted service for third parties without a
//! separate agreement. Operators with a valid signed license unlock
//! additional features through the runtime check exposed by
//! [`LicenseHandle::feature`].
//!
//! Design:
//!
//! 1. **Offline-signed.** Licenses are Ed25519-signed CBOR blobs (see
//!    `forseti-license` repo for the issuer). Forseti verifies
//!    against [`PUBLIC_KEY_BYTES`] — baked in at build time — and never
//!    phones home. Revocation is yearly-renewal-driven; lifetime
//!    licenses are accepted but documented as un-revocable in
//!    `docs/operator-guide.md`.
//!
//! 2. **Single-license model.** One signed blob per Forseti install,
//!    persisted in the `forseti_license` row (singleton PK). Activated
//!    via `/admin/license`; deactivation is row DELETE.
//!
//! 3. **Lock-free reads.** The verified status sits behind an
//!    [`arc_swap::ArcSwap`] on [`AppState`] so every handler's
//!    `feature()` check is a single atomic pointer load.
//!
//! 4. **Grace period.** Expired licenses keep gated features visible
//!    but read-only for `license.grace_days` (default 14) so a
//!    forgotten renewal doesn't blow up production. After the grace
//!    window, gated features hard-gate to the upsell page.
//!
//! See `TODO_LICENSING.md` for the broader scope notes.

