use std::sync::Arc;

use arc_swap::ArcSwap;

/// Ed25519 public key Forseti verifies licenses against. Generated by
/// the `forseti-license` issuer and committed to the repo so reproducible
/// builds emit identical binaries. To rotate, re-key the issuer, replace
/// this file, and re-issue every outstanding license (every prior license
/// becomes invalid after rotation — there's no overlap window).
pub const PUBLIC_KEY_BYTES: &[u8; 32] = include_bytes!("pubkey.bin");

/// Runtime handle. Wraps an [`ArcSwap`] so swaps (activate / deactivate)
/// are atomic and reads are wait-free. Cheap to clone; lives on
/// [`crate::state::AppState`].
#[derive(Clone)]
pub struct LicenseHandle {
    inner: Arc<ArcSwap<LicenseStatus>>,
    /// Grace window applied to expired licenses, in days. Cached at boot
    /// from `config.toml::license.grace_days` so feature checks don't
    /// re-touch the config struct.
    grace_days: i64,
}

impl LicenseHandle {
    pub fn new(initial: LicenseStatus, grace_days: i64) -> Self {
        Self {
            inner: Arc::new(ArcSwap::from_pointee(initial)),
            grace_days,
        }
    }

    /// Snapshot the current status. The returned `Arc` doesn't reflect
    /// later swaps; callers that need a long-lived reference should
    /// re-`load()` rather than caching across awaits.
    pub fn status(&self) -> Arc<LicenseStatus> {
        self.inner.load_full()
    }

    /// Swap in a new status. Called by the activate handler and at boot.
    pub fn swap(&self, next: LicenseStatus) {
        self.inner.store(Arc::new(next));
    }

    pub fn grace_days(&self) -> i64 {
        self.grace_days
    }

    /// Feature gate. Returns [`FeatureStatus::Allowed`] when the license
    /// covers `feature` and isn't past its hard-gate date,
    /// [`FeatureStatus::GraceReadOnly`] inside the grace window,
    /// [`FeatureStatus::Locked`] otherwise.
    pub fn feature(&self, feature: Feature) -> FeatureStatus {
        let status = self.status();
        license::evaluate_feature(&status, feature)
    }
}
